                        <p class="card-text">
                            MAUI staat voor Multi-platform App UI en is een framework dat wordt ontwikkeld door Microsoft. Het is ontworpen om ontwikkelaars in staat te stellen native apps te bouwen die kunnen worden uitgevoerd op verschillende platforms, waaronder iOS, Android, Windows en macOS. <br/><br/>
                            Met MAUI kunnen ontwikkelaars één codebase gebruiken om apps te bouwen die native zijn voor verschillende platforms. Dit betekent dat ze dezelfde UI-componenten, architectuur en logica kunnen delen, waardoor de ontwikkeling efficiënter wordt en de time-to-market wordt verkort. <br/><br/>
                            MAUI bouwt voort op het eerdere Xamarin.Forms-framework en biedt verbeterde mogelijkheden en prestaties. Het maakt gebruik van de .NET MAUI-runtime, die de uitvoering van apps mogelijk maakt op basis van de specifieke platform-API's en functies. <br/><br/>
                            Met MAUI kunnen ontwikkelaars gebruikmaken van een breed scala aan UI-elementen en -controls, toegang krijgen tot native platformfunctionaliteiten en integreren met bestaande .NET-bibliotheken. Ze kunnen ook gebruikmaken van XAML, C# en .NET-ecosysteemtools om hun apps te bouwen. <br/><br/> 
                            Hier wordt geen gebruik gemaakt van HTML voor de view, waardoor de leercurve moeilijker kan zijn. In plaats daarvan wordt XAML gebruikt, wat een specifieke opmaaktaal is voor het bouwen van gebruikersinterfaces. Deze aanpak resulteert in het genereren van native elementen die naadloos passen bij het platform waarop de applicatie wordt uitgevoerd.
