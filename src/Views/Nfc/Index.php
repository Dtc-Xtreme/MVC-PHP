                        <p class="card-text">   
                            Een Android-manifest is een XML-bestand dat wordt gebruikt in Android-applicaties om essentiële informatie over de app te verstrekken aan het Android-besturingssysteem. Het manifest beschrijft de structuur en kenmerken van de app, zoals de naam van het pakket, de toegangsrechten, de vereiste hardware en softwarefuncties, de componenten van de app (zoals activiteiten, services en ontvangers), en meer.<br/><br/>
                            Een specifieke toestemming die relevant kan zijn in het manifest is de NFC-permissie. NFC (Near Field Communication) is een draadloze communicatietechnologie die wordt gebruikt om gegevens over korte afstanden uit te wisselen tussen apparaten. Als een app NFC-functionaliteit gebruikt, moet dit worden vermeld in het Android-manifest. Dit omvat het opnemen van de NFC-permissie om ervoor te zorgen dat de app toegang heeft tot de NFC-mogelijkheden van het apparaat.                        </p>
